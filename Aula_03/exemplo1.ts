namespace exemplo1 {
    // DO WHILE
    let num: number = 19;
    let i: number = 0;
    do {
        console.log(i);
        i++;
        // num = i == 10 ? 19 : 18;
    } while (i <= num);

    // WHILE
    while (i <= 28) {
        // console.log("Entrou no WHILE");
        console.log(i);
        i++;
    }

    // FOR
    for (i = 29; i <= 35; i++) {
        console.log(i);
    }
}
